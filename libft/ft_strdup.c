#include "libft.h"

static char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int		i;
	char	*cpy;

	i = ft_strlen(src);
	cpy = malloc((i + 1) * sizeof(char));
	if (!cpy)
		return (NULL);
	ft_strcpy(cpy, src);
	return (cpy);
}
